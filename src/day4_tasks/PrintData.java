package day4_tasks;

public class PrintData {

    public static void main(String[] args) {

        int month = 5;
        int day = 17;
        int year = 2023;

        System.out.println(month + "_" + day + "_" + year );
        System.out.println(month + "-" + day + "-" + year );
        System.out.println(month + "/" + day + "/" + year );
        System.out.println(month + "|" + day + "|" + year );
        System.out.println(month + "<>" + day + "<>" + year );

    }
}
